<?php

function generateSlug($slug)
{
	$slug = str_replace([
		'Ü', 'ü', 'İ', 'Ö', 'ö', 'Ğ', 'ğ', 'I', 'ı', 'Ə', 'ə', 'Ç', 'ç', 'Ş', 'ş', '(', ')', '/', '?', '!', '@', '%', '&', '"', '“', '”', ' ',
	], [
		'u', 'u', 'i', 'o', 'o', 'g', 'g', 'i', 'i', 'e', 'e', 'c', 'c', 's', 's', '', '', '', '', '', '', '', '', '', '', '', '-',
	], $slug);

	return strtolower($slug);
}
